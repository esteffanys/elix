# palavra_chave.ex

# Criando um mapa usando palavras-chave
pessoa = %{nome: "Alice", idade: 30, cidade: "CidadeA"}

# Acessando valores usando palavras-chave
nome = pessoa[:nome]
idade = pessoa[:idade]
cidade = pessoa[:cidade]

# Imprimindo os resultados
IO.puts "Nome: #{nome}"
IO.puts "Idade: #{idade}"
IO.puts "Cidade: #{cidade}"
